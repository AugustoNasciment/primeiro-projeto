# Calculadora python

num1 = int(input("Digite o primeiro numero: "))

num2 = int(input("Digite o segundo numero: "))

soma= num1 + num2
print(f" O valor da soma {num1} + {num2} = {soma}")
subtracao = num1 - num2
print(f" O valor da subtracao {num1} - {num2} = {subtracao}")
multiplicacao = num1 * num2
print(f" O valor da multiplicacao {num1} * {num2} = {multiplicacao}")
divisao = num1 / num2
print(f" O valor da divisao {num1} * {num2} = {divisao}")
porcentagem = num1 // num2
print(f" O valor da porcentagem {num1} * {num2} = {porcentagem}")
divisao_interia = num1 // num2
print(f" O valor da divisao {num1} * {num2} = {divisao_interia}")
potenciacao = num1 ** num2
print(f" O valor da potenciacao {num1} * {num2} = {potenciacao}")
resto_divisao = num1 % num2
print(f"O valor da resto {num1} * {num2} = {resto_divisao}")
